// ─── Main Component ───────────────────────────────────────────────────────────

export default function TopHeader() {
  const navigation = useNavigation();
  const {
    friendRequests,
    fetchFriendRequests,
    setAddFriendVisible,
    setRequestsVisible,
  } = useFriendStore();

  // Poll friend requests every 10 seconds to keep active notification heart accurate
  useEffect(() => {
    fetchFriendRequests();
    const interval = setInterval(() => {
      fetchFriendRequests();
    }, 10000);
    return () => clearInterval(interval);
  }, []);

  const hasRequests = friendRequests.length > 0;

  return (
    <View style={styles.headerContainer}>
      {/* Left Area: Touchable AppIcon (Taps take user to Dashboard) */}
      <TouchableOpacity
        onPress={() => navigation.navigate('Dashboard' as never)}
        activeOpacity={0.7}
      >
        <AppIcon width={80} height={38} />
      </TouchableOpacity>

      {/* Right Area: Friend controls */}
      <View style={styles.rightGroup}>
        <TouchableOpacity
          onPress={() => setRequestsVisible(true)}
          style={styles.iconButton}
          activeOpacity={0.7}
        >
          {hasRequests ? <ActiveHeart /> : <InactiveHeart />}
        </TouchableOpacity>

        <TouchableOpacity
          onPress={() => setAddFriendVisible(true)}
          style={styles.iconButton}
          activeOpacity={0.7}
        >
          <AddFriendIcon />
        </TouchableOpacity>
      </View>
    </View>
  );
}

